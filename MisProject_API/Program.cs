using Microsoft.EntityFrameworkCore;
using MisProject.DataLayer.Contexts;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddDbContext<MisDbContext>(p =>
{
    p.UseMySql(connectionString: builder.Configuration.GetConnectionString("MisDbContext"),
        ServerVersion.AutoDetect(builder.Configuration.GetConnectionString("MisDbContext")));
});

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
